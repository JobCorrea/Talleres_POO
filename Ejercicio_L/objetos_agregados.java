/*
    CLASE
    La clase fue Negocio, diseñada para representar un negocio real con sus características principales y la capacidad de gestionar productos.

    OBJETOS CREADOS
    Se realizaron cinco objetos distintos:
    - Kinkora -> emprendimiento de cuidado del cabello.
    - Granizados-Don Tubo -> negocio de alimentos y bebidas.
    - La Casa del Sabor -> restaurante.
    - Boutique La Moda -> tienda de ropa.
    - Librería El Saber -> librería.

    ATRIBUTOS
    Cada objeto de la clase Negocio tiene los siguientes atributos:
    - nombreNegocio -> nombre del negocio 
    - ceo -> persona responsable o directora 
    - redSocial -> red social principal 
    - tipoNegocio -> categoría del negocio 
    - productos -> lista de productos que ofrece 

    COMPORTAMIENTOS (métodos)
    Los comportamientos definidos en la clase fueron:
    - mostrarInfo() -> imprime en pantalla todos los datos del negocio y sus productos.
    - agregarProducto(String producto) -> añade un producto nuevo a la lista.
    - quitarProducto(String producto) -> elimina un producto existente de la lista.

*/
